//Assignment Operator

let n1 = 10;
let n2 = 4;

console.log('n1 + n2 = ' , n1 + n2);
console.log('n1 - n2 = ' , n1 - n2);
console.log('n1 * n2 = ' , n1 * n2);
console.log('n1 / n2 = ' , n1 / n2);
console.log('n1 % n2 = ' , n1 % n2);
console.log('++n1 = ',++n1);
console.log('n1++ = ',n1++);
console.log('n1 = ',n1);
console.log('--n1 = ',--n1);
console.log('n1-- = ',n1--);
console.log('n1 = ',n1);

console.log('n1 ** n2 = ',n1 ** n2);

//logical

console.log(true && false);
console.log(true || false);
console.log(!true)

console.log(4>2 && 4<2);
console.log(4>2 || 4<2);
console.log(2>2);

//bitwise

